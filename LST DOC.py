# LIST: they are ordered and unchangeable. duplicate is okay.
fruits_lst = ["apple", "banana", "coconut", "orange"]
print(f"LIST: {fruits_lst}")
print(fruits_lst[0])
print(fruits_lst[1])
print(fruits_lst[2])
print(fruits_lst[3])
print(fruits_lst[0:1])
print(fruits_lst[::-1])
print(fruits_lst[::])
print(len(fruits_lst))
print("pineapple" in fruits_lst)
fruits_lst[0]= "pineapple"
print(fruits_lst)
fruits_lst.append("apple")
print(fruits_lst)
fruits_lst.remove("pineapple")
print(fruits_lst)
fruits_lst.insert(2, "melon")
print(fruits_lst)
fruits_lst.sort()
print(fruits_lst)
fruits_lst.reverse()
print(fruits_lst)
