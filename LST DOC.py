# SET:unordered and immutable, but able to add/ remove. no duplicates
fruits_set = {"apple", "banana", "coconut", "orange"}
print(f"SET: \n{fruits_set}")
#print(dir(fruits_set))
#rint(help(fruits_set))
print(len(fruits_set))
print("pineapple" in fruits_set)
fruits_set.add("pineapple")
